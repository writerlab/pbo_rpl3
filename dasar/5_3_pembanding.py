print("Masukkan bilangan integer 1-10 kedalam variabel 'a' dan 'b'")
a = input('a: ')
b = input('b: ')

# membandingkan dengan operator == (dibaca: sama dengan)
hasil_banding = a == b
print(a,"sama dengan",b,":",hasil_banding)

# membandingkan dengan operator != (tidak sama dengan)
hasil_banding = a != b
print(a,"tidak sama dengan",b,":",hasil_banding)
